var MyBodyColor;
function DarkMode() {
    const backgroundColor = `MyBodyColor.style.backgroundColor`
    return (
        <>

            if (backgroundColor === 'black') {
                `  
                  MyBodyColor.style.backgroundColor = 'green'
                Break`

            }
            else {
                `MyBodyColor.style.backgroundColor = 'black'`
             
            }
        </>
    )
}
export default DarkMode

